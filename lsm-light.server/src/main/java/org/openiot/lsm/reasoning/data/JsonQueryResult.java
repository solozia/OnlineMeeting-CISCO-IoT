package org.openiot.lsm.reasoning.data;

import java.util.HashMap;
import java.util.Map;

public class JsonQueryResult {
	String MEETING_ID = null;
	String USER_ID = null;
	Coordinate LOCATION = null;
	Map<String, String> FEATURES;
	String ThreadId; // for testing
	NFCTagData nfcTagData; //added 2015-09-11
	
	public JsonQueryResult() {
		super();
	}
	public String getMEETING_ID() {
		return MEETING_ID;
	}
	public void setMEETING_ID(String mEETING_ID) {
		MEETING_ID = mEETING_ID;
	}
	public String getUSER_ID() {
		return USER_ID;
	}
	public void setUSER_ID(String uSER_ID) {
		USER_ID = uSER_ID;
	}
	public Map<String, String> getFEATURES() {
		return FEATURES;
	}
	public void setFEATURES(Map<String, String> fEATURES) {
		FEATURES = fEATURES;
	}
	public Coordinate getLOCATION() {
		return LOCATION;
	}
	public void setLOCATION(Coordinate lOCATION) {
		LOCATION = lOCATION;
	}
	public String getThreadId() {
		return ThreadId;
	}
	public void setThreadId(String threadId) {
		ThreadId = threadId;
	}
	public NFCTagData getNfcTagData() {
		return nfcTagData;
	}
	public void setNfcTagData(NFCTagData nfcTagData) {
		this.nfcTagData = nfcTagData;
	}

	
	
	
	
}
