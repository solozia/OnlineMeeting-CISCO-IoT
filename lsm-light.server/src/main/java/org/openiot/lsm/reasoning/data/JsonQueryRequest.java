package org.openiot.lsm.reasoning.data;

import java.util.ArrayList;
import java.util.HashMap;
import java.util.List;
import java.util.Map;

public class JsonQueryRequest {
	String MEETING_ID = null;
	Map<String, String> user = new HashMap();
	Map<String, String> capability = new HashMap();
	
	public JsonQueryRequest() {
		super();
	}

	public JsonQueryRequest(String meetingID, Map<String, String> user,
			Map<String, String> capability) {
		super();
		this.MEETING_ID = meetingID;
		this.user = user;
		this.capability = capability;
	}

	public String getMeetingID() {
		return MEETING_ID;
	}

	public void setMeetingID(String meetingID) {
		this.MEETING_ID = meetingID;
	}

	public Map<String, String> getUser() {
		return user;
	}

	public void setUser(Map<String, String> user) {
		this.user = user;
	}

	public Map<String, String> getCapability() {
		return capability;
	}

	public void setCapability(Map<String, String> capability) {
		this.capability = capability;
	}

	
	
	
	
}
