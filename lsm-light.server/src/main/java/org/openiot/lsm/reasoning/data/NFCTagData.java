package org.openiot.lsm.reasoning.data;

public class NFCTagData {
	String nfcId; 
	String roomName;
	Coordinate coorRoom;
	
	
	public NFCTagData() {
		super();
	}


	public NFCTagData(String nfcId, String roomName, Coordinate coorRoom) {
		super();
		this.nfcId = nfcId;
		this.roomName = roomName;
		this.coorRoom = coorRoom;
	}


	public String getNfcId() {
		return nfcId;
	}


	public void setNfcId(String nfcId) {
		this.nfcId = nfcId;
	}


	public String getRoomName() {
		return roomName;
	}


	public void setRoomName(String roomName) {
		this.roomName = roomName;
	}


	public Coordinate getCoorRoom() {
		return coorRoom;
	}


	public void setCoorRoom(Coordinate coorRoom) {
		this.coorRoom = coorRoom;
	} 
	
	

}
