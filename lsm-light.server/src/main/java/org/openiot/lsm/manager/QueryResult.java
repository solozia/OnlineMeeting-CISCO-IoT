package org.openiot.lsm.manager;
/**
 * @author Thu-Le Pham
 * @date 19 Jan 2016
 * 
 */
public class QueryResult {
	String meetingId;
	String userId;
	String result;

	public QueryResult(String meetingId, String userId, String result) {
		super();
		this.meetingId = meetingId;
		this.userId = userId;
		this.result = result;
	}

	public String getMeetingId() {
		return meetingId;
	}

	public void setMeetingId(String meetingId) {
		this.meetingId = meetingId;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	@Override
	public String toString() {
		return "QueryResult [meetingId=" + meetingId + ", userId=" + userId + ", result=" + result + "]";
	}

}
